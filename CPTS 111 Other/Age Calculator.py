years = int(input("Please enter your age in years: "))

months = 12 * years
days = 365 * years
hours = 24 * days
minutes = 60 * hours
seconds = 60 * minutes

print("You have been alive for:", months,"Months")
print("You have been alive for:", days,"Days")
print("You have been alive for:", hours,"Hours")
print("You have been alive for:", minutes,"Minutes")
print("You have been alive for:", seconds,"Seconds")
