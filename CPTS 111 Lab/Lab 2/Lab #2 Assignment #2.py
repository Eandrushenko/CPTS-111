#Prompt the user for strings
say_something = input("Say something: ")
say_more = input("Say some more: ")

#Tell the user the length of the strings
print("The first thing you typed is", len(say_something), "Characters long.")
print("The second thing you typed is", len(say_more), "Characters long.")

#Combine the user's strings
print("Your two strings put together are:", (say_something) + (say_more))

#Tell the user that the program is complete
print("Program Complete")

      


